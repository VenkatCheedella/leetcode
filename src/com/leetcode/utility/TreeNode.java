package com.leetcode.utility;

public class TreeNode {
	int val;
	public TreeNode left = null;
	public TreeNode right = null;
	
	public TreeNode(int val) {
		this.val = val;
	}
	
	public void setLeft(TreeNode left) {
		this.left = left;
	}
	
	public void setRight(TreeNode right) {
		this.right = right;
	}
	
}
